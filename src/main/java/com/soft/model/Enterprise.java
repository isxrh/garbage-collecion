package com.soft.model;

public class Enterprise {
    private Integer Id;

    private String Enterprise_name;
    private String Enterprise_account;
    private String Enterprise_password;
    private String Enterprise_address;
    private String Enterprise_phone;
    private String Enterprise_image;
    private String Enterprise_introduce;
	public Integer getId() {
		return Id;
	}
	public void setId(Integer id) {
		this.Id = id;
	}
	public String getEnterprise_name() {
		return Enterprise_name;
	}
	public void setEnterprise_name(String enterprise_name) {
		Enterprise_name = enterprise_name;
	}
	public String getEnterprise_account() {
		return Enterprise_account;
	}
	public void setEnterprise_account(String enterprise_account) {
		Enterprise_account = enterprise_account;
	}
	public String getEnterprise_password() {
		return Enterprise_password;
	}
	public void setEnterprise_password(String enterprise_password) {
		Enterprise_password = enterprise_password;
	}
	public String getEnterprise_address() {
		return Enterprise_address;
	}
	public void setEnterprise_address(String enterprise_address) {
		Enterprise_address = enterprise_address;
	}
	public String getEnterprise_phone() {
		return Enterprise_phone;
	}
	public void setEnterprise_phone(String enterprise_phone) {
		Enterprise_phone = enterprise_phone;
	}
	public String getEnterprise_image() {
		return Enterprise_image;
	}
	public void setEnterprise_image(String enterprise_image) {
		Enterprise_image = enterprise_image;
	}
	public String getEnterprise_introduce() {
		return Enterprise_introduce;
	}
	public void setEnterprise_introduce(String enterprise_introduce) {
		Enterprise_introduce = enterprise_introduce;
	}
}